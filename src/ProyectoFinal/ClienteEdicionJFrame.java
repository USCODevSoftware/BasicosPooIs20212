    private void actualizarClienteBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarClienteBtActionPerformed
        // TODO add your handling code here:
        Cliente c =new Cliente();
        //Controlar el evento cuando ingresan caracteres??
        c.setId(Integer.parseInt(idClienteTf.getText()));
        c.setNombre(nombreClienteTf.getText());
        c.setEmail(emailClienteTf.getText());
        c.setMovil(movilClienteTf.getText());
        c.setMontoMaximo(Double.parseDouble(montoClienteTf.getText()));
        ListIterator<Cliente> listIter = listClientes.listIterator();
        Cliente cl = null;
        while (listIter.hasNext()) {
            cl = listIter.next();
            if (cl.getId()==c.getId())
            {
                //listIter.remove();
                listIter.set(c);
            }
        }        
        ClienteCRUD cCrud = new ClienteCRUD();
        cCrud.actualizarCliente(listClientes);
      
        
    }//GEN-LAST:event_actualizarClienteBtActionPerformed

