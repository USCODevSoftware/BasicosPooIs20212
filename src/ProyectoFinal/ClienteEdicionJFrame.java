    private void seleccionarClienteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleccionarClienteBtnActionPerformed
        // TODO add your handling code here:
        indexup = clientesLista.getSelectedIndex();
        ListModel lModel= clientesLista.getModel();
        clup = (Cliente)lModel.getElementAt(indexup);
        idClienteTf.setText(String.valueOf(clup.getId()));
        emailClienteTf.setText(clup.getEmail());
        montoClienteTf.setText(String.valueOf(clup.getMontoMaximo()));;
        movilClienteTf.setText(clup.getMovil());;
        nombreClienteTf.setText(clup.getNombre());        
        
    }//GEN-LAST:event_seleccionarClienteBtnActionPerformed

